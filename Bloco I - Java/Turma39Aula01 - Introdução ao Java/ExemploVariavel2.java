package br.com.generation.aula01;

public class ExemploVariavel2 {
	
	public static void main(String[] args) {
		
		int var1;
		double var2;
		
		var1 = 10;
		var2 = 10.0;
		
		System.out.println("Valor Original da variável 1: " + var1);
		System.out.println("Valor Original da variável 2: " + var2);
		
		var1 = var1 / 4;
		var2 = var2 / 4;
		
		System.out.println("Valor da variável 1 após a divisão: " + var1);
		System.out.println("Valor da variável 1 após a divisão: " + var2);
		
	}

}
